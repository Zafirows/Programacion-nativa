fun main() { // Define la función principal, que es el punto de entrada de cualquier programa Kotlin.
    println("Hello, world!") // Imprime el texto "Hello, world!" en la consola, seguido de un salto de línea.

    val count: Int = 2 // Declara una variable inmutable 'count' de tipo entero y le asigna el valor 2.
    println(count) // Imprime el valor de la variable 'count' en la consola.

    val unreadCount = 5 // Declara una variable inmutable 'unreadCount' y le asigna el valor 5 (mensajes no leídos).
    val readCount = 100 // Declara una variable inmutable 'readCount' y le asigna el valor 100 (mensajes leídos).

    // Imprime el total de mensajes sumando los leídos y no leídos, usando interpolación de variables.
    println("You have ${unreadCount + readCount} total messages in your inbox.")
}

